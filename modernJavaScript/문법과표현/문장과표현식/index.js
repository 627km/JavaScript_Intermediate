/** 
 * 문장 (Statements)
 * : 자바스크립트에서 문장은 어떤동작이 일어나도록 작성된 최소한의 코드 덩어리를 가리킨다. 
 * : 선언문, 할당문, 조건문, 반복문 .. 이렇게  끝에 문이라고 붙는 이유가 모두 동작을 수행하는 문장이기 때문이다. 
 */

/**
 * 표현식 (Expressions)
 * : 자바스크립트에서 표현식은 결과적으로 하나의 값이 되는 모든 코드를 가리킨다.
 * : 어떤 하나의 값을 그대로 작성하는 것도 표현식이지만, 연산자를 이용한 연산식도 결국 하나의 값이 되고, 선언된 변수를 호출하거나, 
 * : 객체의 프로퍼티에 접근하는 것도 결국에는 하나의 값으로 평가된다. 그래서 길이와는 상관없이 결과적으로 하나의 값이 되는 코드를 모두 표현식이라고 한다.
 */

// 자바스크립트에서 특별한 경우를 제외하면 일반적으로 표현식인 문장은 세미콜론으로, 표현식이 아닌 문장은 문장 자체의 코드 블록 (중괄호)로 그 문장의 범위가 구분된다. 