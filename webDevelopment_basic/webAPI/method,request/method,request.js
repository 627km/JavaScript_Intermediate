// 메소드 (method)
/**
 * 요청(request)의 종류 - 메소드 
 * 1. 데이터 조회 - Get -> Get Request
 * 2. 데이터 추가 - Post -> Post Request
 * 3. 데이터 수정 - Put -> Put Request
 * 4. 데이터 삭제 - Delete -> Delete Request
 */

/**
 * Request (하나의 request는 head와 body로 구성된다.)
 * - Head : request에 대한 부가 정보를 담고 있음 (method)
 * - body : 실제 데이터를 담는 부분 (get(조회), delete(삭제) 메소드는 body가 없다.)
 */