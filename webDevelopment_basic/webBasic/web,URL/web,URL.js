// Web - World Wide Web의 줄임말로 전세계적인 연결망을 의미

/* 
URL - Uniform Resource Locator의 줄임말 (웹에 존재하는 특정 데이터를 나타내는 문자열)
<URL의 구성> 
ex) https://www.codeitshopping.com/men/shirts?color=blue&size=m
호스트(Host): 전 세계 서버 중 하나의 서버를 특정 (https://www.codeitshopping.com)
경로(Path): 서버에 있는 데이터 중 원하는 데이터를 특정 (/men/shirts)
쿼리(Query): 데이터에 관한 세부적인 요구사항 (물음표 뒤에 오는 부분. 있을수도 있고 없을수도 있다. color=blue&size=m)
*/