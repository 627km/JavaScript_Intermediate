/**
 * Rest API (개발자 면접질문으로 자주 등장)
 * : 오늘날 많은 웹 개발자들이 Web API 설계할 때, 준수하기 위해 노력하는 일종의 가이드라인이다.
 * 
 * REST Architecture
 * : 미국의 컴퓨터 과학자인 Roy Fielding이 본인의 박사논문에서 제시한 개념인데, 그는 웹이 갖추어야 할 이상적인 아키텍처(구조)로 REST architecture라는 개념을 제시했다.
 *   REST는 Representational State Transfer(표현적인 상태 이전)의 줄임말로, 해석하면 '표현적인, 상태 이전'이라는 뜻이다. 
 * 
 * REST architecture이 되기위한 조건
 * 1. Client-Server
 * 2. Stateless
 * 3. Cache
 * 4. Uniform Interface
 * 5. Layered System
 * 6. Code on Demand
 */